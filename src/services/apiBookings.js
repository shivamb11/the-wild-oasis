import supabase from "./supabase";

import { PAGE_SIZE } from "../utils/constants.js";
import { getToday } from "../utils/helpers.js";

export async function getBookings({ filterObj, sortObj, pageObj }) {
  let query = supabase
    .from("bookings")
    .select("*, cabins(*), guests(*)", { count: "exact" })
    .range(
      (pageObj.currentPage - 1) * PAGE_SIZE,
      pageObj.currentPage * PAGE_SIZE - 1
    );

  if (filterObj.value !== "all") {
    query = query.eq(filterObj.field, filterObj.value);
  }

  if (sortObj.field !== "all") {
    query = query.order(sortObj.field, { ascending: sortObj.value });
  }

  const { data, error, count } = await query;

  if (error) {
    throw new Error("Error while loading the bookings.");
  }

  return { data, count };
}

export async function getBooking(id) {
  const { data, error } = await supabase
    .from("bookings")
    .select("*, cabins(*), guests(*)")
    .eq("id", id)
    .single();

  if (error) {
    throw new Error("Booking not found");
  }

  return data;
}

// Returns all BOOKINGS that are were created after the given date. Useful to get bookings created in the last 30 days, for example.
// date: ISOString
export async function getBookingsAfterDate(date) {
  const { data, error } = await supabase
    .from("bookings")
    .select("created_at, totalPrice, extrasPrice")
    .gte("created_at", date)
    .lte("created_at", getToday({ end: true }));

  if (error) {
    throw new Error("Bookings could not get loaded");
  }

  return data;
}

// Returns all STAYS that are were created after the given date
export async function getStaysAfterDate(date) {
  const { data, error } = await supabase
    .from("bookings")
    .select("*, guests(fullName)")
    .gte("startDate", date)
    .lte("startDate", getToday());

  if (error) {
    throw new Error("Bookings could not get loaded");
  }

  return data;
}

